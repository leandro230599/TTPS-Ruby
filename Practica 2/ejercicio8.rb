# Qué diferencia hay entre el uso de include y extend a la hora de incorporar un módulo en una clase?
# 1. Si quisieras usar un módulo para agregar métodos de instancia a una clase, ¿qué forma usarías a la hora de incorporar el módulo a la clase?
# 2. Si en cambio quisieras usar un módulo para agregar métodos de clase, ¿qué forma usarías en ese caso?

# Include se utiliza para modulos de instancia y el extend para modulos de clase
# 1- Si quisiera usar un modulo para agregar metodos de instancia a una clase, la incorporaria mediante el include
# 2- Si quisiera usar un modulo para agregar metodos de clase usaria el extend